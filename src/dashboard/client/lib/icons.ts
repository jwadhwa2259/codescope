/**
 * Returns an icon SVG string at the specified size.
 * Default is 20x20 for sidebar, pass 16 for inline use.
 */
export function icon(name: keyof typeof icons, size: 16 | 20 = 20): string {
  const svg = icons[name];
  if (size === 20) return svg;
  return svg
    .replace(/width="20"/, `width="${size}"`)
    .replace(/height="20"/, `height="${size}"`);
}
